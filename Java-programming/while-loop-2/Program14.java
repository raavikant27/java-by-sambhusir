class Program14 {
    public static void main(String[] args) {
        
        int i = 1;
        double sum = 0.0;
        while (i <= 100) {
           
            sum=sum+ (1.0/(2*i));
            i++;
        }
        System.out.println("print the sum  series from 1 to 100 = "+ sum);
    }
}



